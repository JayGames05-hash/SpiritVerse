export function findFeastFastForDate(date = new Date()) {
  const month = String(date.getMonth() + 1).padStart(2, '0')
  const day = String(date.getDate()).padStart(2, '0')
  const key = `${month}-${day}`

  const normalized = FASTS_FEASTS_2026.map(item => ({
    ...item,
    normalizedDate: item.feast_date.trim().replace(/\s+/g, ' ')
  }))

  return normalized.find(item => {
    const rangeParts = item.normalizedDate.split('-').map(part => part.trim())
    if (rangeParts.length === 1) {
      const dateParts = rangeParts[0].split(' ')
      if (dateParts.length !== 2) return false
      const monthKey = String(new Date(`${dateParts[0]} 1, 2026`).getMonth() + 1).padStart(2, '0')
      const dayKey = String(Number(dateParts[1])).padStart(2, '0')
      return `${monthKey}-${dayKey}` === key
    }

    const startParts = rangeParts[0].split(' ')
    const endParts = rangeParts[1].split(' ')
    if (startParts.length !== 2 || endParts.length !== 2) return false

    const startDate = new Date(`2026 ${startParts[0]} ${Number(startParts[1])}`)
    let endDate = new Date(`2026 ${endParts[0]} ${Number(endParts[1])}`)
    if (endDate < startDate) {
      endDate = new Date(`2027 ${endParts[0]} ${Number(endParts[1])}`)
    }

    const currentDate = new Date(date.getFullYear(), date.getMonth(), date.getDate())
    const start = new Date(startDate.getFullYear(), startDate.getMonth(), startDate.getDate())
    const end = new Date(endDate.getFullYear(), endDate.getMonth(), endDate.getDate())

    return currentDate >= start && currentDate <= end
  })
}

export default FASTS_FEASTS_2026
